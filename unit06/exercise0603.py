g = int(input("g: "))
if g < 60:
   print("Bad!")
elif 60 <= g < 70:
   print("Not Bad")
elif 70 <= g < 80:
   print("Good!")
elif 80 <= g < 90:
   print("Great!")
elif 90 <= g < 100:
   print("Excellent!")
elif g == 100:
   print("Perfect!")
else:
   print("Pardon?")
 
